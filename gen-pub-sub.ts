async function generate(schemaPath: string, outputDir: string): Promise<void> {
  const raw = fs.readFileSync(schemaPath, 'utf8')
  const schema: Schema = yaml.parse(raw)
  const dir = schema.config?.output_dir ?? outputDir
  const dbSchema = normalizeDbSchema(schema.config?.db_schema)
  const tableNames = Object.keys(schema.tables)



  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true })


  const writeFile = async (path: string, content: string) => {
    await fs.promises.mkdir(path.split('/').slice(0, -1).join('/'), { recursive: true })
    await fs.promises.writeFile(path, content)
  }

  let needsQueue = false
  const analyzedTables: Array<{ name: string; analysis: TableAnalysis }> = []

  for (const [name, table] of Object.entries(schema.tables)) {
    const analysis = analyzeTable(name, table)
    analyzedTables.push({ name, analysis })
    if (analysis.hasQueueTrigger) needsQueue = true

    // SQL migration
    const sql = generateSQL(name, table, analysis, tableNames, dbSchema)
    await writeFile(path.join(dir, `sql/${name}.sql`), sql)
    console.log(`  âœ“ ${name}.sql`)

    const policiesSql = generatePoliciesSQL(name, analysis, dbSchema)
    if (policiesSql) {
      await writeFile(path.join(dir, `policies/${name}.policy.sql`), policiesSql)
      console.log(`  âœ“ ${name}.policy.sql`)
    }

    // TypeScript types
    const types = generateTypes(name, table, analysis)
    await writeFile(path.join(dir, `types/${name}-types.ts`), types)
    console.log(`  âœ“ ${name}-types.ts`)

    // TypeScript SDK
    const sdk = generateSDK(name, table, analysis, dbSchema)
    await writeFile(path.join(dir, `sdk/${name}-sdk.ts`), sdk)
    console.log(`  âœ“ ${name}-sdk.ts`)
  }

  const supportsRole = (analysis: TableAnalysis, role: 'anon' | 'authenticated' | 'service_role'): boolean => {
    if (role === 'anon') return analysis.accessRoles.length === 0 || analysis.accessRoles.includes('anon')
    return analysis.accessRoles.includes(role)
  }

  const roleOrder: Array<'anon' | 'authenticated' | 'service_role'> = ['anon', 'authenticated', 'service_role']
  for (const role of roleOrder) {
    const roleTables = analyzedTables.filter(t => supportsRole(t.analysis, role))
    if (!roleTables.length) continue
    const example = generateExampleFile(schema, roleTables, dbSchema, role)
    const fileName = `${role}.example.ts`
    await writeFile(path.join(dir, fileName), example)
    console.log(`  âœ“ ${fileName}`)
  }

  const legacyExamplePath = path.join(dir, 'example.ts')
  if (fs.existsSync(legacyExamplePath)) {
    await fs.promises.unlink(legacyExamplePath)
  }

  const realtimeEligibleTables = analyzedTables.filter(t => t.analysis.hasRealtimeSubscription)
  if (realtimeEligibleTables.length) {
    const subscriber = generateRealtimeSubscriberExampleFile(realtimeEligibleTables, dbSchema)
    await writeFile(path.join(dir, 'subscriber.example.ts'), subscriber)
    console.log(`  âœ“ subscriber.example.ts`)

    const invokeChange = generateRealtimeInvokeChangeExampleFile(realtimeEligibleTables, dbSchema)
    await writeFile(path.join(dir, 'invoke_change.example.ts'), invokeChange)
    console.log(`  âœ“ invoke_change.example.ts`)
  } else {
    const subscriberPath = path.join(dir, 'subscriber.example.ts')
    if (fs.existsSync(subscriberPath)) {
      await fs.promises.unlink(subscriberPath)
    }
    const invokeChangePath = path.join(dir, 'invoke_change.example.ts')
    if (fs.existsSync(invokeChangePath)) {
      await fs.promises.unlink(invokeChangePath)
    }
  }

  // Shared queue infrastructure (generated once if any table needs it)
  if (needsQueue) {
    await writeFile(path.join(dir, 'queue.sql'), genQueueInfra(dbSchema))
    console.log(`  âœ“ queue.sql (shared queue infrastructure)`)
  }

  console.log(`\nâœ… Generated ${Object.keys(schema.tables).length} table(s) â†’ ${dir}/`)
}

